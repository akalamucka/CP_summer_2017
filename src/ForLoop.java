public class ForLoop {

    public static void main(String[] args) {


        for (int i = 0; i < 10; i++) {
            System.out.println("i=" + i);
        }

        {
            int k = 0;
            while (k < 10) {
                System.out.println("k=" + k);
                k++;
            }
            int m = 10;
            do {
                System.out.println("m" + m);
                m--;

            } while (m < 5);


            //liczba kropek w zaleznosci o dliczby wierszy


        }
    }
}




